    <h1 class="header">OVERVIEW</h1>
    <p class="textLeft default-blue textBold">
        Tea Small Holdings Development Authority (TSHDA) was established on 1st February 1977, 
        under the Tea Small Holdings Development Act No. 35 of 1975. Act No. 36 of 1991 to establish and register 
        the Tea Small Holder Societies and Act No. 21 of 1997 to grant legal status to these societies have been subsequently legislated. 
        Act No. 34 of 2003 adding amendments to the original act have been legislated.As the sole statutory body mandated for the development 
        of tea small holdings in the country, increase of production, marketing activities, improvement of productivity and working for the 
        welfare of the tea small holders are the objects of the organization.
    </p>
    <p class="textLeft default-blue textBold">
        The lowest unit at the grassroots level of the service provision structure is the Tea Inspector / Extension Officer range. 
        147 such ranges are currently operated throughout the tea growing area, which are immediately supervised by the sub office structure consisting 
        of 26 sub offices in eight regions. These regions are guided and supervised by the Head Office at the national level.
    </p>
    <p class="textLeft default-blue textBold">
        The Tea Smallholder Development Society system greatly assists the service provision at the grassroots level by the organization. 
        The society system has a similar hierarchy and operates parallel to the organization.
    </p>
</div>

